1- El objetivo del testing de software es garantizar la calidad, la confiabilidad y el correcto funcionamiento del software, proporcionando a los usuarios un producto final sólido, satisfactorio y libre de errores.

Se lleva a cabo durante el ciclo de vida del desarrollo del software para identificar errores, defectos o problemas en el sistema y garantizar que cumpla con los requisitos establecidos y las expectativas del usuario.

2-Realizar testing sobre un software no puede demostrar de manera absoluta que el software funcione correctamente. El testing de software tiene como objetivo identificar errores, defectos o problemas en el sistema, pero no puede garantizar la ausencia total de errores puesto que no es exhaustivo.

3- El testing de caja negra es una técnica de prueba de software que se centra en evaluar y probar la funcionalidad del sistema sin conocer su implementación interna. En este enfoque, el tester se enfoca únicamente en las entradas y salidas del software, sin considerar los detalles de cómo se realiza el procesamiento interno.

La principal característica del testing de caja negra es que se realiza desde una perspectiva externa al sistema.

4- No, contar con una especificación del programa es esencial para realizar el testing de caja negra de manera efectiva y adecuada. Proporciona la información necesaria para el diseño de casos de prueba, la definición de criterios de aceptación, la comunicación y el consenso, y asegura una cobertura completa y precisa de las funcionalidades del software.








